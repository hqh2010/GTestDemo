#include "Function.h"

/*
 * 计算两个整型数据相加
 *
 * @param opNum1: 操作数1
 * @param opNum2: 操作数2
 *
 * @return int: 返回两数相加的结果
 */
int Function::add(int opNum1, int opNum2)
{
    return opNum1 + opNum2;
}

/*
 * 计算两个整型数据相减
 *
 * @param opNum1: 操作数1
 * @param opNum2: 操作数2
 *
 * @return int: 返回两数相减的结果
 */
int Function::minus(int opNum1, int opNum2)
{
    return opNum1 - opNum2;
}